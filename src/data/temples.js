/** 지역별 필터 옵션 */
export const REGIONS = ["전체", "서울", "경기", "강원", "충청", "전라", "경상", "제주"];

/** 방문 통계 계산 */
export const getStats = (visitedIds = []) => {
  const total = temples.length; // 108
  const visited = visitedIds.length;
  const percent = total > 0 ? Math.round((visited / total) * 100) : 0;
  const UNESCO_count = temples.filter((t) => t.UNESCO).length;
  const visitedUNESCO = temples.filter((t) => t.UNESCO && visitedIds.includes(t.id)).length;

  return { total, visited, percent, UNESCO_count, visitedUNESCO };
};

/** 지역별 사찰 수 */
export const getRegionCounts = () => {
  return REGIONS.filter((r) => r !== "전체").map((region) => ({
    region,
    count: temples.filter((t) => t.region === region).length,
  }));
};

